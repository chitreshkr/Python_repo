# Examples of Relational Operators
a = int(input("Enter first number : "))
b = int(input("Enter second number : "))

# a > b is False
print(a > b)

# a < b is True
print(a < b)

# a == b is False
print(a == b)

# a != b is True
print(a != b)

# a >= b is False
print(a >= b)

# a <= b is True
print(a <= b)